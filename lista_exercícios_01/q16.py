# Inicialize uma variável chamada "macas" com o valor 5.
macas = 5

# Inicialize uma variável chamada "laranjas" com o valor 10.
laranjas = 10

# Inicialize uma variável chamada "frutas" com a soma de "macas" e "laranjas".
frutas = macas + laranjas

# Imprime o valor atual de "frutas"
print(f'Valor inicial de frutas: {frutas}')

# Reatribua a variável "macas" para ser 20.
macas = 20

# Recalcule a variável "frutas" da mesma forma que anteriormente.
frutas = macas + laranjas

# Imprime o novo valor de "frutas"
print(f'Novo valor de frutas: {frutas}')


