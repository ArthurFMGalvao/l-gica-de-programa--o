# Conversão de True para str
str_val = str(True)
print(f"True para str: {str_val}")

# Conversão de 3 para float
float_val = float(3)
print(f"3 para float: {float_val}")

# Conversão de 3.8 para str
str_val_2 = str(3.8)
print(f"3.8 para str: {str_val_2}")

# Conversão de 0.5 para int
int_val = int(0.5)
print(f"0.5 para int: {int_val}")

# Conversão de "4" para int
int_val_2 = int("4")
print(f'"4" para int: {int_val_2}')